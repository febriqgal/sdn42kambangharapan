import { Menu, Transition } from "@headlessui/react";
import { Fragment, useEffect, useRef, useState } from "react";
import { signOut } from "firebase/auth";
import { getAuth } from "firebase/auth";
import { Button } from "@nextui-org/react";
import { useRouter } from "next/router";

export default function ProfileC() {
  const route = useRouter();
  const auth = getAuth();
  const user = getAuth().currentUser;
  return (
    <div className="">
      <Menu as="div" className="relative inline-block text-left">
        <div>
          <Menu.Button className={"bg-gray-200 rounded-full p-1"}>
            <svg
              width="24"
              height="24"
              viewBox="0 0 24 24"
              fill="none"
              xmlns="http://www.w3.org/2000/svg"
            >
              <ellipse
                opacity="0.4"
                cx="12"
                cy="17"
                rx="7"
                ry="4"
                fill="#28303F"
              />
              <circle cx="12" cy="7" r="4" fill="#28303F" />
            </svg>
          </Menu.Button>
        </div>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="absolute right-0 w-56 mt-2 origin-top-right bg-white divide-y divide-gray-100 rounded-md shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none">
            <div className="flex flex-col gap-2 px-5 py-10">
              <h1 className="font-bold text-center">
                {user?.displayName ?? ""}
              </h1>
              <Menu.Item>
                {user?.email != "admin@gmail.com" ? (
                  <></>
                ) : (
                  <Button
                    onPress={() => {
                      route.push("/admin");
                    }}
                    flat
                    className="text-black"
                    size={"sm"}
                  >
                    Dashboard Admin
                  </Button>
                )}
              </Menu.Item>
              <Menu.Item>
                {user ? (
                  <Button
                    onPress={async () => {
                      await signOut(auth);
                      route.reload();
                    }}
                    className="w-full text-white bg-red-500"
                    size={"sm"}
                  >
                    Keluar
                  </Button>
                ) : (
                  <Button
                    onPress={() => {
                      route.push("/login");
                    }}
                    flat
                    bordered
                    className="bg-[#172554] text-white w-full"
                    size={"sm"}
                  >
                    Login
                  </Button>
                )}
              </Menu.Item>
            </div>
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  );
}

